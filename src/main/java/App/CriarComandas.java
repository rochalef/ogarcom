    private Comandas novaComanda;
    
    private void addComandaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addComandaActionPerformed
        Comandas comanda = new Comandas();
        ComandasBO cbo = new ComandasBO();
        int mesa = Integer.parseInt(numMesa.getText());
        String nome_pessoa = nomeCliente.getText();
        int id = Integer.parseInt(idGarcom.getText());
        comanda.setNomePessoa(nome_pessoa);
        comanda.setIdGarcom(id);
        comanda.setMesa(mesa);
        cbo.addComanda(comanda);
        novaComanda = comanda;
        this.dispose();
    }//GEN-LAST:event_addComandaActionPerformed

