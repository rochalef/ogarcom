    private void FecharComandaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FecharComandaActionPerformed
        int idFechar = Integer.parseInt(idComanda.getText()); 
        boolean resposta = true;

        Comandas comandaParaFechar = null;
        
        List<Comandas> lc = comandasBO.listarAbertas();
        for(Comandas comanda : lc){
            if(idFechar == comanda.getId()){
                comandaParaFechar = comanda;
            }  
        }
        
        if (comandaParaFechar == null) {
            JOptionPane.showMessageDialog(this, "Comanda não encontrada!");
            return;
        }

        PedidosBO pedBO = new PedidosBO();
        comandaParaFechar.setListaPedidos(pedBO.listarPedidos(idFechar));


        float totalPedidos = 0;
        for (Pedidos p : comandaParaFechar.getListaPedidos()) {
            totalPedidos += p.getPrecoProduto() * p.getQtd();
        }

        float precoFinal = totalPedidos;
        

        comandaParaFechar.setPrecoFinal(precoFinal);

        ComandasDAO cdao = new ComandasDAO();
        cdao.fecharComanda(comandaParaFechar); 

        comandasBO.listarAbertas();
        comandasBO.listarFechadas();
        
        JOptionPane.showMessageDialog(this, 
            "Comanda fechada com sucesso");

        resetTabela = true;

        this.dispose();

    }//GEN-LAST:event_FecharComandaActionPerformed

