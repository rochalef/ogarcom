    public void carregarTabelaPedidos(int idComandaP){
        PedidosBO pbo = new PedidosBO();
        List<Pedidos> listaPedidosComanda = pbo.listarPedidos(idComandaP);
        DefaultTableModel model = (DefaultTableModel) tabelaPedidos.getModel();
        model.setRowCount(0);
        for (Pedidos pedido : listaPedidosComanda){
            Object[] novaLinha = {pedido.getId(), pedido.getNomeProduto(), pedido.getQtd(), pedido.getPrecoProduto(), (pedido.getPrecoProduto()*pedido.getQtd())};
            model.addRow(novaLinha);
        }
    }
    public void recarregarTabelaPedidos() {
        carregarTabelaPedidos(idComandaP);
        tabelaPedidos.revalidate();
        tabelaPedidos.repaint();
    }

